package utils;

/**
 * @author grolfsen
 *
 */
public class Coordinates {
	public final int x;
	public final int y;

	public Coordinates(final int x, final int y) {
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return this.x;
	}

	public int getY() {
		return this.y;
	}

}
